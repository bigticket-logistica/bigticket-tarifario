export const pct = (n, decimals = 1) =>
  (n * 100).toFixed(decimals) + '%'

export const pctSigned = (n, decimals = 1) =>
  (n >= 0 ? '+' : '') + (n * 100).toFixed(decimals) + '%'

export const PROJ_MONTHS = [
  'Abr 26', 'May 26', 'Jun 26', 'Jul 26',
  'Ago 26', 'Sep 26', 'Oct 26', 'Nov 26',
  'Dic 26', 'Ene 27', 'Feb 27', 'Mar 27',
]

export function buildProjections(v) {
  const base = { cob: 20986, pag: 7577 }
  return PROJ_MONTHS.map((m, mi) => {
    const t = (mi + 1) / 12
    const scenarios = {
      opt: { d: 1411 + (1100 - 1411) * t, u: 965 + (920 - 965) * t, ipc: 0.048 + (0.035 - 0.048) * t },
      base: { d: 1411 * (1 + 0.005 * mi), u: 965 * (1 + 0.003 * mi), ipc: 0.048 + (0.065 - 0.048) * t },
      pes: { d: 1411 + (1800 - 1411) * t, u: 965 + (1080 - 965) * t, ipc: 0.048 + (0.095 - 0.048) * t },
    }
    const calc = (sc) => {
      const fv = { ...v, diesel: sc.d, usd: sc.u, ipc: sc.ipc * 100 }
      const f = calcFactor(fv)
      return calcTarifa(base.cob, base.pag, f.total, v.margen, false).nc
    }
    return {
      m,
      d_o: Math.round(scenarios.opt.d),
      d_b: Math.round(scenarios.base.d),
      d_p: Math.round(scenarios.pes.d),
      t_o: calc(scenarios.opt),
      t_b: calc(scenarios.base),
      t_p: calc(scenarios.pes),
    }
  })
}
